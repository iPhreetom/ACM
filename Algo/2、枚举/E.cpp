#include<bits/stdc++.h>
#define int long long
using namespace std;

int num;
int a[] = {2,3,5,7};
int b[] = {1,3,7,9};

bool is_prime(int n){
	for(int i=2;i<=sqrt(n);i++){
		if(n%i == 0)return false;
	}
	return true;
}

void digui(int n,bool first,int sum){
	int m = sum;
	if(first){
		if(n == 1){
			for(int i=0;i<4;i++){
				m = a[i];
				if(is_prime(m)){
					cout<<m<<'\n';
				}
			}
		}
		else{
			for(int i=0;i<4;i++){
				m = sum*10;
				m += a[i];
				if(is_prime(m))digui(n-1,0,m);
			}
		}
	}
	else{
		if(n == 1){
			for(int i=0;i<4;i++){
				m = sum*10;
				m += b[i];
				if(is_prime(m)){
					cout<<m<<'\n';
				}
			}
		}
		else{
			for(int i=0;i<4;i++){
				m = sum*10;
				m += b[i];
				if(is_prime(m))digui(n-1,0,m);
			}
		}
	}
}


signed main(){
	ios::sync_with_stdio(false),cin.tie(0),cout.tie(0);

	cin>>num;

	digui(num,1,0);



	return 0;
}
