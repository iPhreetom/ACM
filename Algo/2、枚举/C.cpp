#include<bits/stdc++.h>
#define int long long
#define double long double
using namespace std;


vector<double> arr;
double a,b,c,d;
int ans = 0;


double f(double t){
	return a*t*t*t + b*t*t + c*t + d;
}

void digui(double l,double r){
	if(ans >= 3){
		return ;
	}
	double mid = l+(r-l)/2;
	if(f(l)*f(r) < 0){
		if(abs(l-r) < abs(1e-3)){
			arr.push_back(l);
			return ;
		}
		else{
			digui(mid,r);
			digui(l,mid);
		}
	}
	return ;
}

signed main(){
	ios::sync_with_stdio(false),cin.tie(0),cout.tie(0);


	cin>>a>>b>>c>>d;
	// cout<<f(2)<<endl;
	double x1 = -200;
	double x2 = 200;

	for(int i=-100;i<=100 && ans<3;i++){
		if(abs(f(i)) < 1e-2){
			arr.push_back(i);
		}
		else{
			if(f(i)*f(i+1) < 0){
				digui(i,i+1);
			}
		}
	}

	for(int i=0;i<arr.size();i++){
		if(i!=0 && abs(arr[i]-arr[i-1]) >= 1)cout<<fixed<<setprecision(2)<<arr[i]<<' ';
		if(i == 0)cout<<fixed<<setprecision(2)<<arr[i]<<' ';
	}cout<<endl;


	return 0;
}
